    if [ $? -eq 0 ]; then
        log_success "–í–µ—Ä—Å–∏—è Python $PYTHON_VERSION —Å–æ–≤–º–µ—Å—Ç–∏–º–∞"
    else
        log_error "–¢—Ä–µ–±—É–µ—Ç—Å—è Python $REQUIRED_VERSION –∏–ª–∏ –≤—ã—à–µ. –¢–µ–∫—É—â–∞—è –≤–µ—Ä—Å–∏—è: $PYTHON_VERSION"
